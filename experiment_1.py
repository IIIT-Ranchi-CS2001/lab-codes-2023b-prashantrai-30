# WAP to find sum,differencr,product,integer quotient,remainder,fractional quotient of two number.Enter the number on run time,display the input data and result near format

num1 = int(input("Enter first Number:"))
num2 = int(input("Enter second Number:"))

# SUM
print("sum of",num1,"and",num2,"is",num1+num2)

# DIFFERENCE
print("difference of",num1,"and",num2,"is",num1-num2)

# PRODUCT
print("product of",num1,"and",num2,"is",num1*num2)

# INTEGER QUOTIENT 
print("integer quotient of",num1,"and",num2,"is",(num1//num2))

# REMAINDER
print("remainder of",num1,"and",num2,"is",num1%num2)

# FRACTIONAL QUOTIENT
print("fractional quotient of",num1,"and",num2,"is",num1/num2)

